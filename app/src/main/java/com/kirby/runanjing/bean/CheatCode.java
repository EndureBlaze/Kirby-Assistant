package com.kirby.runanjing.bean;

public class CheatCode
{
	private String id;
	private String cheatCode;
	public CheatCode(String id, String cheatCode)
	{
		this.id = id;
		this.cheatCode = cheatCode;
	}
	public String getId()
	{
		return id;
	}
	public String getCheatCode()
	{
		return cheatCode;
	}
}
